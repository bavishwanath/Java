package basics;

public class StringBasedProgrammes {

	public static void main(String[] args) {
		

		String s = "the java script code";
		
		StringBuilder sb = new StringBuilder("samwell");
		System.out.println(sb);
		sb.reverse();
		System.out.println(sb);
		
		StringBuffer sbf = new StringBuffer("tarly");
		System.out.println(sbf);
		sbf.reverse();
		System.out.println(sbf);
		
		
	}

}
